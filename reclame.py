def gemiddelde(mijn_lijst):
    mijn_lijst = 220, 430, 125, 160, 90, 345
    totaal = sum(mijn_lijst)
    aantal = len(mijn_lijst)
    gemiddeld = totaal / aantal

    uitvoer = f"De gemiddelde inkomsten deze week zijn {gemiddeld} euro"
    return uitvoer

print(gemiddelde(0))


    
    
    
  
      
   
  
 
    
    

